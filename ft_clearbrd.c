#include "fillit.h"

void		ft_clearbrd(char *str, char c)
{
	int i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			if (str[i] == c)
			{
				str[i] = '.';
				i++;
			}
			else
				i++;
		}
	}
}
