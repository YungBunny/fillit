#include "fillit.h"

void		ft_getindx(t_list *tet, char *str)
{
	int	s;
	int	a;

	s = 0;
	a = 0;
	if (str)
	{
		while (str[s])
		{
			if (str[s] == '.')
				s++;
			else
			{
				tet->char_spots[a++] = s;
				s++;
			}
		}
	}
}
